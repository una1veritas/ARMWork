/**
  * @brief  This function handles TIM3 global interrupt request.
  * @param  None
  * @retval None
  */
void TIM7_IRQHandler(void)
{
  if (TIM_GetITStatus(TIM7, TIM_IT_Update) != RESET)
  {   
    STM_EVAL_LEDToggle(LED4);
    
    TIM_ClearITPendingBit(TIM7, TIM_IT_Update);
  } 
}


